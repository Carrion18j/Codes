let color = "blue";

switch (color) {
  case "red":
    console.log("The color is red.");
    break;
  case "blue":
    console.log("The color is blue.");
    break;
  case "green":
    console.log("The color is green.");
    break;
  default:
    console.log("The color is not red, blue, or green.");
}
